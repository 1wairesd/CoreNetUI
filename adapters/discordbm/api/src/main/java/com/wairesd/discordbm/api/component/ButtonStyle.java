package com.wairesd.discordbm.api.component;

/**
 * Enum representing the style of a Discord button
 */
public enum ButtonStyle {
    /**
     * Primary button (blue)
     */
    PRIMARY,
    
    /**
     * Secondary button (gray)
     */
    SECONDARY,
    
    /**
     * Success button (green)
     */
    SUCCESS,
    
    /**
     * Danger button (red)
     */
    DANGER,
    
    /**
     * Link button (opens a URL)
     */
    LINK
} 